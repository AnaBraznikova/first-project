public class SecondProgramm {
    public static void main(String[] args) {
        System.out.println("Это уже второй урок");

        // +-*/% - основные операции
        System.out.println(5 / 2);//2
        System.out.println(45 / 4);//11
        System.out.println(5.0 / 2);//2.5
        System.out.println(5 / 2.0);//2.5
        System.out.println(5 % 2);//1   //остаток при делении
        System.out.println(7 % 5);//2
        System.out.println(15 % 3);//0

        System.out.println(4 + 5);//9
        System.out.println(4.1 + 5);//9.1
        System.out.println(4.0 + 5.0);//9.0
        System.out.println(4.5 + 4.5);//9.0
        System.out.println(567.6 + 498.7 - 66.5);//999.8


        System.out.println(456 + 345);//801
        System.out.println(34 * 67);//2278
        System.out.println(121 % 11);//0


        System.out.println(1 - 0.9);//0.099999999998 // сам копм не может записать чило 0.1 у нег ос этим проблемы,именно 0 и 1
        System.out.println(1 - 0.7);//0.30000000004 // проблемы дробных чисел, то, что меньше 1
        System.out.println(1 - 0.5);//0.5


    }
}


